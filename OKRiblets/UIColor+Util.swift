import UIKit

extension UIColor {
    
    static func colorBlue3() -> UIColor {
        return UIColor(red: 93/255, green: 143/255, blue: 222/255, alpha: 1)
    }
    
    static func colorGray7() -> UIColor {
        return UIColor(red: 241/255, green: 241/255, blue: 245/255, alpha: 1)
    }
}
